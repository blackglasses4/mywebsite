    <h1>&mdash; O mnie</h1>
    <p>Witam, jestem Adrianna Pawłowska, 3D designer oraz Web developer z pasją. 
        Obecnie mieszkam w Poznaniu, jednym z miast w Polsce, gdzie przez ostatnie dwadzieścia lat,
        od samego początku mojej drogi, pasjonuję się odkrywaniem i rozwijaniem moich najskrytszych
        umiejętności. Na przestrzeni czasu zauważyłam, że ciągnie mnie do komputerów oraz projektowaniu
        przeróżnych projektów w dziedzinie grafiki. Dlategoteż postanowiłam podjąć wyzwanie, poświęcić 
        swój czas na naukę oraz samodzielnie doskonalić się w świecie trójwymiarowym, tworząc przy tym 
        unikalne strony internetowe. Obecnie studiuję już trzeci rok Informatykę ze specjalnością grafika
        i multimedia na Uniwersytecie Merito, która poprzednio miała nazwę Wyższa Szkoła Bankowa w Poznaniu.
    </p>
    <p>
        Zależy mi ogromnie na zdobyciu praktycznych umiejętności oraz rozwijaniu się w zawodzie grafika 3D
        oraz Web developer. Choć obecnie większość mojego czasu poświęcam się pracy tym dwom dziedziną, 
        to moja pasja do grafiki rozkwitła na płaszczyźnie 2D, gdzie zaczęła się moja wspaniała przygoda z 
        tym fascynującym światem. Na przestrzeni lat osobiście zdobyłam umiejętności pracy z językami
        programistycznymi oraz darmowymi programami graficznymi takich jak Inskape, Gimp, Krita, Blender,
        C#, Python, HTML, CSS, JavaScript oraz GTFL. Dzięki tym umiejętnością chętnie podejmę każde wyzwanie,
        które pozwoli mi poszerzyć swoje horyzonty i odkryć nowe możliwości. 
    </p>
    <p>
        Uważam się za osobę ambitną, kreatywną oraz zdeterminowaną, która w razie jakichkolwiek niepowodzeń,
        zawsze stara się szybko rozwiązać je. Modelowanie 3D oraz tworzenie stron internetowych nie jest tylko
        zwykłą pracą dla mnie, lecz prawdziwą pasją. Z nich czerpię radość oraz pozwalam się zatracić
        wyobraźni. Wierzę, że z biegiem czasu, kształcąc się i łącząc estetykę, prostotę oraz funkcjonalność
        w mych pracach będę w stanie stworzyć coś niezwykłego i wyróżniającego się sposród innych użytkowników.
    </p>
